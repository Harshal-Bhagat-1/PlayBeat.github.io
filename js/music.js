    var sound=document.getElementById("audio");
    var icon=document.getElementById("icon");
    icon.onclick = function(){
      if(sound.paused){
        sound.play();
        icon.src="images/pause.png";
      }
      else{
        sound.pause();
        icon.src="images/play2.png";
    }
}

